// Default theme
let currentTheme = PINK_AUTUMN;

// Helper function to get current theme colors
export const getColors = () => currentTheme;

// Function to set theme dynamically
export const setCurrentTheme = (themeKey, isDarkMode = false) => {
  let selectedTheme;

  if (isDarkMode) {
    selectedTheme = THEME_MAP[themeKey + '_DARK'] || THEME_MAP[themeKey];
  } else {
    selectedTheme = THEME_MAP[themeKey] || ORANGE_DRIP;
  }

  currentTheme = selectedTheme;
  return currentTheme;
};